package promedio;

public class Calculadora {
    public int suma (int a, int b) {
        int suma = a + b;
        return suma;
    }
    
    public int resta (int a, int b) {
        int resta = a - b;
        return resta;
    }
    
    public int multiplicacion (int a, int b) {
        int multiplicacion = a * b;
        return multiplicacion;
    }
    
    public double division (int a, int b) {
        double division = a / b;
        return division;
    }
    
}
